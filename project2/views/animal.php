    <h2>Zakopanéban
Tavaly egy medve elpusztult, mert felébredt a szilveszteri mulatság zajától. A nemzeti park is figyelmeztetett, hogy a zaj és a tűzijáték fényei megijeszthetik az állatokat.
Több tízezren írták alá a petíciót a lengyel síparadicsomban, Zakopanéban, amelyben az ellen tiltakoztak, hogy a város megváltoztatta a hagyományos szilveszteri koncert és tűzijáték helyszínét. A tiltakozók szerint a városon kívüli hegyoldalra helyezett rendezvény megzavarhatja a medvék téli álmát.

A város lakói egy helyi újságból tudták meg, hogy a városi tanács októberben megtárgyalta a lengyel köztévé által is közvetített ünnepség áthelyezését a belvárosból a hegy lábához, a Tátrai Nemzeti Parkba, a híres Wielka Krokiew síugrósánchoz.

Miután a polgármester elismerte, hogy ilyen döntés született, múlt szombaton több száz tüntető vonult végig a városon egy fekvő medvebábút cipelve. A tiltakozók azt követelték, hogy a polgármester vonja vissza a döntést.

Az újság által indított petíciót keddre már több mint 27 ezren írták alá.
A szilveszteri rendezvényt élőben közvetítő TVP közszolgálati tévécsatorna "ál-környezetvédőnek" nevezte a tiltakozókat, és azzal érvelt, hogy a sportrendezvényeket is gond nélkül lebonyolítják a térségben.

Az újság cikkét követően

a nemzeti park is közleményt adott ki, amelyben hangsúlyozták, hogy a zaj és a tűzijáték fényei megijeszthetik az állatokat, ami miatt elvándorolhatnak és hosszú távon a populációjuk is csökkenhet. A közlemény rámutatott, hogy tavaly elpusztult egy medve, amely felébredt a szilveszteri mulatság zajától.

A park szakemberei elmondták, hogy a hegyen kárpáti barna medvék élnek, bár azt, hogy mekkora a populáció, nem tudják pontosan.

Az AFP jelentése szerint a zakopanei polgármester pénteken azt mondta, hogy engedtek a közvélemény nyomásának, és a köztévével megegyezve idén is az óvárosban rendezik meg a szilveszteri koncertet.
